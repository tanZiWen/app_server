package com.prosnav.model;

public class Cust {
	private String custId;
	private String custCell;
	private String custIdnum;
	private String custEmail;
	private String salesName;
	private String salesId;
	private String salesPhone;
	private String isInvestor;
	
	public String getIsInvestor() {
		return isInvestor;
	}
	public void setIsInvestor(String isInvestor) {
		this.isInvestor = isInvestor;
	}
	public String getCustId() {
		return custId;
	}
	public void setCustId(String custId) {
		this.custId = custId;
	}
	public String getCustCell() {
		return custCell;
	}
	public void setCustCell(String custCell) {
		this.custCell = custCell;
	}
	public String getCustIdnum() {
		return custIdnum;
	}
	public void setCustIdnum(String custIdnum) {
		this.custIdnum = custIdnum;
	}
	public String getCustEmail() {
		return custEmail;
	}
	public void setCustEmail(String custEmail) {
		this.custEmail = custEmail;
	}
	public String getSalesName() {
		return salesName;
	}
	public void setSalesName(String salesName) {
		this.salesName = salesName;
	}
	public String getSalesId() {
		return salesId;
	}
	public void setSalesId(String salesId) {
		this.salesId = salesId;
	}
	public String getSalesPhone() {
		return salesPhone;
	}
	public void setSalesPhone(String salesPhone) {
		this.salesPhone = salesPhone;
	}
	
}
